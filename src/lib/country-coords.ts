/**
 * Given an array of ISO alpha-2 country codes, returns the geographic centroid
 * as [latitude, longitude]. Falls back to [0, 0] if none are recognized.
 */
export function getCentroidFromCountryCodes(countryCodes: string[]): [number, number] {
  const known = countryCodes
    .map((c) => COUNTRY_CENTROIDS[c.toUpperCase()])
    .filter(Boolean) as [number, number][];

  if (known.length === 0) return [0, 0];

  const lat = known.reduce((s, c) => s + c[0], 0) / known.length;
  const lng = known.reduce((s, c) => s + c[1], 0) / known.length;
  return [lat, lng];
}
